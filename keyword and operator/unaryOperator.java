public class unaryOperator {
    public static void main(String[] args) {
        int counter = 0;
        System.out.print("Counter is: "+counter);
        System.out.print("\n++Counter is: "+ ++counter);
        System.out.print("\nCounter is: "+counter);

        System.out.println();
        System.out.println();

        counter = 0;

        System.out.print("Counter is: "+counter);
        System.out.print("\nCounter++ is: "+counter++);
        System.out.print("\nCounter is: "+counter);
    }
}
