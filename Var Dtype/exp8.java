public class exp8 {
    public static void main(String[] args) {
        int x = 11;
        int y = 5;

        System.out.println(x);
        System.out.println(y);

        System.out.println("Without Casting- x/y = "+(x/y));
        System.out.println("After Casting- x/y = "+((float)x/y));
    }
}
